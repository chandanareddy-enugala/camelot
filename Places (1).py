from Functions.Actions import Actions

# %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
# ====================================================== PLACES CREATION ===========================================================
# %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
class Place:
    # Defining Characteristics of Place
    # =================================================================================================================================
    def __init__(self, place):
        # place = {"Name", "Type"}
        self.Name = place["Name"]
        self.Type = place["Type"]

        self.Create_Place()

    # Creating a Place
    # =================================================================================================================================
    def Create_Place(self):
        self.action = Actions()
        self.action.run_command('CreatePlace' + '(' + self.Name + ', ' + self.Type + ')', True)